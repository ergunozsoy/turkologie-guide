const faq = document.getElementById("faq");
const searchInput = document.getElementById("searchInput");

function renderFAQ(filter = "") {
  faq.innerHTML = "";
  const term = filter.trim().toLowerCase();

  faqData.forEach(group => {
    const matches = group.items.filter(([q, a]) =>
      q.toLowerCase().includes(term) || a.toLowerCase().includes(term) || group.category.toLowerCase().includes(term)
    );

    if (matches.length === 0) return;

    const heading = document.createElement("h2");
    heading.className = "category";
    heading.textContent = group.category;
    faq.appendChild(heading);

    matches.forEach(([q, a]) => {
      const item = document.createElement("article");
      item.className = "faq-item";
      item.innerHTML = `
        <button class="question" type="button">${q}</button>
        <div class="answer"><p>${a}</p></div>
      `;
      faq.appendChild(item);
    });
  });
}

faq.addEventListener("click", event => {
  if (!event.target.classList.contains("question")) return;
  event.target.closest(".faq-item").classList.toggle("open");
});

searchInput.addEventListener("input", event => renderFAQ(event.target.value));
renderFAQ();
